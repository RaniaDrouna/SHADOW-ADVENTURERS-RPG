class CharacterStats {
    int strength;
    int dexterity;
    int intelligence;
    int vitality;
    int wisdom;
    int luck;

    public CharacterStats(int str, int dex, int intel, int vit, int wis, int lck) {
        this.strength = str;
        this.dexterity = dex;
        this.intelligence = intel;
        this.vitality = vit;
        this.wisdom = wis;
        this.luck = lck;
    }
}